from enum import Enum


# +-------------------------------------------------------------------------+
# |                                  Enum                                   |
# +-------------------------------------------------------------------------+

class Color(Enum):
    RESET = "\033[0m"
    BOLD = "\033[1m"
    UNDERLINE = "\033[4m"
    REVERSE = "\033[7m"

    BLACK = "\033[30m"
    RED = "\033[31m"
    GREEN = "\033[32m"
    YELLOW = "\033[33m"
    BLUE = "\033[34m"
    MAGENTA = "\033[35m"
    CYAN = "\033[36m"
    LIGHT_GRAY = "\033[37m"

    DARK_GRAY = "\033[90m"
    BRIGHT_RED = "\033[91m"
    BRIGHT_GREEN = "\033[92m"
    GOLD = "\033[93m"
    SKY_BLUE = "\033[94m"
    PINK = "\033[95m"
    TURQUOISE = "\033[96m"
    WHITE = "\033[97m"

    ORANGE = "\033[38;5;208m"
    CORAL = "\033[38;5;203m"
    LIME = "\033[38;5;118m"

    def __str__(self):
        return self.value
