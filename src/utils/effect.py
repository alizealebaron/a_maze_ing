from enum import Enum


# +-------------------------------------------------------------------------+
# |                                  Enum                                   |
# +-------------------------------------------------------------------------+

class Effect(Enum):
    """
    An enumeration of terminal effects that can be applied to text.
    """

    RESET = "\033[0m"
    BOLD = "\033[1m"
    UNDERLINE = "\033[4m"
    REVERSE = "\033[7m"

    def __str__(self):
        return self.value
